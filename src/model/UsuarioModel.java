package model;	

 public class UsuarioModel {	

 	private int usuario;		
	private String perfil; 	


 	public int getUsuario() {	
		return usuario;	
	}	

 	public UsuarioModel setUsuario(int usuario) {	
		this.usuario = usuario ;	
		return this;
	}	

 	public String getPerfil() {	
		return perfil;	
	}	
	public UsuarioModel setPerfil(String perfil) {	
		this.perfil = perfil;	
		return this;
	}	

 }	
